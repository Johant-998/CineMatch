function parseMvs(raw) {
  const seen = new Set();
  return raw.trim().split('\n').map(ln => {
    const p = ln.split('|');
    if (p.length < 4) return null;
    const id = parseInt(p[0]);
    if (seen.has(id)) return null;
    seen.add(id);
    return { id, title: p[1].trim(), year: p[2].trim(), genres: p[3].split(',') };
  }).filter(Boolean);
}
const MOVIES = parseMvs(RAW);

