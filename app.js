// Puntaje de similitud entre dos películas
function similarity(a, b) {
  const sharedGenres = a.genres.filter(g => b.genres.includes(g)).length;
  const totalGenres  = new Set([...a.genres, ...b.genres]).size;
  const genreScore   = totalGenres > 0 ? sharedGenres / totalGenres : 0;
  const yearDiff     = Math.abs((+a.year || 1990) - (+b.year || 1990));
  const yearScore    = Math.max(0, 1 - yearDiff / 30);
  return genreScore * 0.75 + yearScore * 0.25;
}

// Recomendación basada en películas seleccionadas
function recommendBySimilar(seeds, count = 10) {
  const seedIds = new Set(seeds.map(m => m.id));
  const scores  = {};
  MOVIES.forEach(m => {
    if (seedIds.has(m.id)) return;
    scores[m.id] = seeds.reduce((acc, s) => acc + similarity(s, m), 0) / seeds.length;
  });
  return MOVIES
    .filter(m => !seedIds.has(m.id))
    .sort((a, b) => (scores[b.id] || 0) - (scores[a.id] || 0))
    .slice(0, count);
}

// Recomendación basada en géneros seleccionados
function recommendByGenres(genres, count = 10) {
  const scored = MOVIES.map(m => {
    const matches = genres.filter(g => m.genres.includes(g)).length;
    return { movie: m, score: matches / genres.length };
  }).filter(x => x.score > 0);
  // Shuffle top results for variety
  const top = scored.sort((a, b) => b.score - a.score).slice(0, count * 3);
  shuffle(top);
  return top.slice(0, count).map(x => x.movie);
}

// Recomendación general (películas variadas por género)
function recommendGeneral(count = 10) {
  const allGenres = [...ALL_G];
  const result = [];
  const used   = new Set();
  // Pick 1-2 from each genre for variety
  for (const g of allGenres) {
    if (result.length >= count) break;
    const candidates = MOVIES.filter(m => !used.has(m.id) && m.genres.includes(g));
    if (candidates.length === 0) continue;
    const pick = candidates[Math.floor(Math.random() * Math.min(candidates.length, 20))];
    result.push(pick);
    used.add(pick.id);
  }
  shuffle(result);
  return result.slice(0, count);
}

function shuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
}

// Genera una razón de recomendación sin API
function makeReason(movie, seeds, genres) {
  const g = GES[movie.genres[0]] || movie.genres[0];
  if (seeds && seeds.length > 0) {
    const bestSeed = seeds.reduce((best, s) =>
      similarity(s, movie) > similarity(best, movie) ? s : best, seeds[0]);
    return `Similar a "${bestSeed.title}" por sus elementos de ${g}.`;
  }
  if (genres && genres.length > 0) {
    const matched = movie.genres.filter(mg => genres.includes(mg)).map(mg => GES[mg] || mg);
    return `Encaja con tu gusto por ${matched.join(' y ')}.`;
  }
  return `Un clásico del cine de ${g} que no deberías perderte.`;
}

