function openModal(id) {
  const movie=MOVIES.find(m=>m.id===id); if(!movie) return;
  const synopsis=SYNOPSES[id]||null;
  document.getElementById('moverlay').classList.add('open');
  document.getElementById('mbody').innerHTML=`
    <div class="minner">
      <div class="mposter-modal">${genPoster(movie)}</div>
      <div>
        <div class="mmtitle">${movie.title}</div>
        <div class="mmyear">📅 ${movie.year}</div>
        <div class="mmgenres">${movie.genres.map(g=>`<span class="mmgtag">${GES[g]||g}</span>`).join('')}</div>
        ${synopsis
          ? `<div class="mmdesc">${synopsis}</div>`
          : `<div class="mmloading" id="mls"><span class="mspin"></span>&nbsp;Cargando sinopsis...</div>
             <div class="mmdesc" id="mld" style="display:none"></div>`}
      </div>
    </div>`;
  if(!synopsis) fetchSynopsis(movie);
}

async function fetchSynopsis(movie) {
  // For movies not in our pre-loaded SYNOPSES, show a generated description
  const setDesc=(txt)=>{
    const ls=document.querySelector('.mmloading'),desc=document.getElementById('mld');
    if(ls)ls.style.display='none';
    if(desc){desc.textContent=txt;desc.style.display='block';}
  };
  const g = movie.genres[0] || 'Drama';
  const genEs = GES[g] || g;
  setDesc(`${movie.title} (${movie.year}) es una película de ${genEs} que forma parte del catálogo clásico de cine.`);
}

